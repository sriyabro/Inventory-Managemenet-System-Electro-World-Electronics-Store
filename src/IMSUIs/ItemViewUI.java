    private void DFelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DFelBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) ItemTable.getModel();
        int row = ItemTable.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(row, 0).toString());
        
        int resDialog = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "ITEM DETAILS", JOptionPane.YES_NO_OPTION);
        
        if (resDialog == JOptionPane.YES_OPTION){
            try {
                //initialized on top

                sta = con.prepareStatement("DELETE FROM item WHERE item_id=?");
                sta.setInt(1,id);

                sta.executeUpdate();
                JOptionPane.showMessageDialog(this, "Item deleted Successfully ", "ITEM DETAILS", JOptionPane.INFORMATION_MESSAGE);
                ResetForm();
                UpdateTable();
                
            } catch (SQLException ex) {
                Logger.getLogger(UserRegUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_DFelBtnActionPerformed

    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) ItemTable.getModel();
        int row = ItemTable.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(row, 0).toString());
        String IName = NameTxt.getText();
        String Mod = ModTxt.getText();
        String Cat = ItemCatCmb.getSelectedItem().toString();
        String Sup = SupTxt.getText();
        String War = WarCmb.getSelectedItem().toString();
        String SPrice = SPriceTxt.getText();

        if (Pattern.matches("^[a-zA-Z0-9]+[@]+[a-zA-Z0-9]+[.]+[a-zA-Z0-9]+$", SupTxt.getText())) {
            try {
                //initialized on top

                sta = con.prepareStatement("UPDATE item SET name=?,model=?,category=?,sup_id=?,warranty=?,sell_price=? WHERE item_id=?");
                sta.setString(1, IName);
                sta.setString(2, Mod);
                sta.setString(3, Cat);
                sta.setString(4, Sup);
                sta.setString(5, War);
                sta.setString(6, SPrice);
                sta.setInt(7, id);

                sta.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Details Updated Successfully ", "CUSTOMER DETAILS", JOptionPane.INFORMATION_MESSAGE);
                ResetForm();
                UpdateTable();
                //resets the form
            } catch (SQLException ex) {
                Logger.getLogger(UserRegUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Email Address", "CUSTOMER DETAILS", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

