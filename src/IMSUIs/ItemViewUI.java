    public void UpdateTable() {
        try {
            int c;
            sta = con.prepareStatement("SELECT * FROM item");
            ResultSet res = sta.executeQuery();

            ResultSetMetaData rsd = res.getMetaData();
            c = rsd.getColumnCount();

            DefaultTableModel model1 = (DefaultTableModel) ItemTable.getModel();
            model1.setRowCount(0);

            while (res.next()) {
                Vector vec = new Vector();
                for (int i = 0; i <= c; i++) {
                    vec.add(res.getString("Item_id"));
                    vec.add(res.getString("name"));
                    vec.add(res.getString("model"));
                    vec.add(res.getString("category"));
                    vec.add(res.getString("sup_id"));
                    vec.add(res.getString("warranty"));
                    vec.add(res.getString("sell_price"));
                }
                model1.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(ItemViewUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

