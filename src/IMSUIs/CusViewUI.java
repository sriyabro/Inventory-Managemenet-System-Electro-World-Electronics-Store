    public void UpdateTable() {
        try {
            int c;
            sta = con.prepareStatement("SELECT * FROM customer");
            ResultSet res = sta.executeQuery();

            ResultSetMetaData rsd = res.getMetaData();
            c = rsd.getColumnCount();

            DefaultTableModel model1 = (DefaultTableModel) CusTable.getModel();
            model1.setRowCount(0);

            while (res.next()) {
                Vector vec = new Vector();
                for (int i = 0; i <= c; i++) {
                    vec.add(res.getString("cus_id"));
                    vec.add(res.getString("f_name"));
                    vec.add(res.getString("l_name"));
                    vec.add(res.getString("address"));
                    vec.add(res.getString("city"));
                    vec.add(res.getString("email"));
                    vec.add(res.getString("M_No"));
                    vec.add(res.getString("W_No"));
                }
                model1.addRow(vec);
            }

        } catch (SQLException ex) {
            Logger.getLogger(CusViewUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

