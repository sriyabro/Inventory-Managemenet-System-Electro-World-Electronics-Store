    private void DFelBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DFelBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) CusTable.getModel();
        int row = CusTable.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(row, 0).toString());
        
        int resDialog = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this record?", "CUSTOMER DETAILS", JOptionPane.YES_NO_OPTION);
        
        if (resDialog == JOptionPane.YES_OPTION){
            try {
                //initialized on top

                sta = con.prepareStatement("DELETE FROM customer WHERE cus_id=?");
                sta.setInt(1,id);

                sta.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Details deleted Successfully ", "CUSTOMER DETAILS", JOptionPane.INFORMATION_MESSAGE);
                ResetForm();
                UpdateTable();
                
            } catch (SQLException ex) {
                Logger.getLogger(UserRegUI.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        }
    }//GEN-LAST:event_DFelBtnActionPerformed

    private void UpdateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateBtnActionPerformed
        DefaultTableModel model = (DefaultTableModel) CusTable.getModel();
        int row = CusTable.getSelectedRow();

        int id = Integer.parseInt(model.getValueAt(row, 0).toString());
        String FName = FNameTxt.getText();
        String LName = LNameTxt.getText();
        String Add = AddTxt.getText();
        String City = CityTxt.getText();
        String Email = EmailTxt.getText();
        String MNo = ContMTxt.getText();
        String WNo = ContWTxt.getText();

        if (Pattern.matches("^[a-zA-Z0-9]+[@]+[a-zA-Z0-9]+[.]+[a-zA-Z0-9]+$", EmailTxt.getText())) {
            try {
                //initialized on top

                sta = con.prepareStatement("UPDATE customer SET f_name=?,l_name=?,address=?,city=?,email=?,M_No=?,W_No=? WHERE Cus_id=?");
                sta.setString(1, FName);
                sta.setString(2, LName);
                sta.setString(3, Add);
                sta.setString(4, City);
                sta.setString(5, Email);
                sta.setString(6, MNo);
                sta.setString(7, WNo);
                sta.setInt(8, id);

                sta.executeUpdate();
                JOptionPane.showMessageDialog(this, "Customer Details Updated Successfully ", "CUSTOMER DETAILS", JOptionPane.INFORMATION_MESSAGE);
                ResetForm();
                UpdateTable();
                //resets the form
            } catch (SQLException ex) {
                Logger.getLogger(UserRegUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please Enter a Valid Email Address", "CUSTOMER DETAILS", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_UpdateBtnActionPerformed

